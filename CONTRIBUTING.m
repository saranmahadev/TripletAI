# Contributing to TripletAI

Thank you for considering contributing to TripletAI! We welcome contributions from the community to help improve the project.

## How to Contribute

1. **Fork the Repository**  
    Fork the repository to your GitHub account and clone it to your local machine.

2. **Create a Branch**  
    Create a new branch for your feature or bug fix:
    ```
    git checkout -b feature/your-feature-name
    ```

3. **Make Changes**  
    Implement your changes in the codebase. Ensure your code follows the project's coding standards.

4. **Write Tests**  
    Add tests to verify your changes. Make sure all existing and new tests pass.

5. **Commit Changes**  
    Write clear and concise commit messages:
    ```
    git commit -m "Add a brief description of your changes"
    ```

6. **Push Changes**  
    Push your branch to your forked repository:
    ```
    git push origin feature/your-feature-name
    ```

7. **Submit a Pull Request**  
    Open a pull request to the main repository. Provide a detailed description of your changes and link any related issues.

## Code of Conduct

Please adhere to our [Code of Conduct](CODE_OF_CONDUCT.md) to ensure a welcoming and inclusive environment for everyone.

## Reporting Issues

If you encounter any issues, please report them in the [Issues](https://github.com/TripletAI/tripletai/issues) section of the repository. Provide as much detail as possible to help us address the problem.

## Contact

If you have any questions or need further assistance, feel free to reach out to the maintainers.

Thank you for contributing to TripletAI!